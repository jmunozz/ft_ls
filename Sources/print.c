#include "ft_ls.h"

void	ft_print_arg(t_meta **meta, char *str, int i)
{
	if (i > 1)
	{
		if ((*meta)->first != 0)
			ft_putchar('\n');
		ft_putstr(str);
		ft_putstr(":\n");
		((*meta)->first)++;
	}
}
